const assert = require('assert');
const { withMongoDB } = require('../../../../helpers/test-db');
const { newComment } = require('../../../../helpers/data/dataset');
const generateSessions = require('../../../../../jobs/import/sessions/generateSessions');

describe(__filename, withMongoDB(({ getTestDatabase, insertIntoDatabase, importIntercarif }) => {

    it('should reconcile sessions with comments', async () => {

        let db = await getTestDatabase();
        let date = new Date();
        let comment = newComment({
            formacode: '22403',
            training: {
                formacode: '22403',
                certifInfo: {
                    id: '80735',
                },
                organisation: {
                    siret: '22222222222222',
                },
                place: {
                    postalCode: '75019',
                },
            }
        }, date);

        await Promise.all([
            importIntercarif(),
            insertIntoDatabase('comment', comment),
        ]);

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.deepEqual(session, {
            _id: 'F_XX_XX|AC_XX_XXXXXX|SE_XXXXXX',
            numero: 'SE_XXXXXX',
            region: '11',
            avis: [comment],
            score: {
                nb_avis: 1,
                notes: {
                    accueil: 3,
                    contenu_formation: 2,
                    equipe_formateurs: 4,
                    moyen_materiel: 2,
                    accompagnement: 1,
                    global: 2
                }
            },
            meta: {
                reconciliation: {
                    organisme_formateur: '22222222222222',
                    lieu_de_formation: '75019',
                    certifinfos: [
                        '80735'
                    ],
                    formacodes: [
                        '22403'
                    ]
                },
                source: {
                    type: 'intercarif',
                    numero_session: 'SE_XXXXXX',
                    numero_formation: 'F_XX_XX',
                    numero_action: 'AC_XX_XXXXXX',
                }
            }
        });
    });

    it('should create session with empty avis list when no comment can be found', async () => {

        let db = await getTestDatabase();
        await importIntercarif();

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.deepEqual(session, {
            _id: 'F_XX_XX|AC_XX_XXXXXX|SE_XXXXXX',
            numero: 'SE_XXXXXX',
            region: '11',
            score: {
                nb_avis: 0
            },
            meta: {
                reconciliation: {
                    organisme_formateur: '22222222222222',
                    lieu_de_formation: '75019',
                    formacodes: ['22403'],
                    certifinfos: ['80735']
                },
                source: {
                    type: 'intercarif',
                    numero_session: 'SE_XXXXXX',
                    numero_formation: 'F_XX_XX',
                    numero_action: 'AC_XX_XXXXXX',
                },
            },
        });
    });

    it('should reconcile comments with same formace/siret/code_postal than the session', async () => {
        let db = await getTestDatabase();
        let date = new Date();
        let comment = newComment({
            formacode: '22403',
            training: {
                formacode: '22403',
                certifInfo: null,
                organisation: {
                    siret: '22222222222222',
                },
                place: {
                    postalCode: '75019',
                },
            }
        }, date);

        await Promise.all([
            importIntercarif(),
            insertIntoDatabase('comment', comment),
        ]);

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.deepEqual(session, {
            _id: 'F_XX_XX|AC_XX_XXXXXX|SE_XXXXXX',
            numero: 'SE_XXXXXX',
            region: '11',
            avis: [comment],
            score: {
                nb_avis: 1,
                notes: {
                    accueil: 3,
                    contenu_formation: 2,
                    equipe_formateurs: 4,
                    moyen_materiel: 2,
                    accompagnement: 1,
                    global: 2
                }
            },
            meta: {
                reconciliation: {
                    organisme_formateur: '22222222222222',
                    lieu_de_formation: '75019',
                    certifinfos: [
                        '80735'
                    ],
                    formacodes: [
                        '22403'
                    ]
                },
                source: {
                    type: 'intercarif',
                    numero_session: 'SE_XXXXXX',
                    numero_formation: 'F_XX_XX',
                    numero_action: 'AC_XX_XXXXXX',
                }
            }
        });
    });

    it('should reconcile comments with same certifinfo/siret/code_postal than the session', async () => {

        let db = await getTestDatabase();
        let date = new Date();
        let comment = newComment({
            formacode: null,
            training: {
                formacode: null,
                certifInfo: { id: '80735' },
                organisation: {
                    siret: '22222222222222',
                },
                place: {
                    postalCode: '75019',
                },
            }
        }, date);

        await Promise.all([
            importIntercarif(),
            insertIntoDatabase('comment', comment),
        ]);

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.deepEqual(session, {
            _id: 'F_XX_XX|AC_XX_XXXXXX|SE_XXXXXX',
            numero: 'SE_XXXXXX',
            region: '11',
            avis: [comment],
            score: {
                nb_avis: 1,
                notes: {
                    accueil: 3,
                    contenu_formation: 2,
                    equipe_formateurs: 4,
                    moyen_materiel: 2,
                    accompagnement: 1,
                    global: 2
                }
            },
            meta: {
                reconciliation: {
                    organisme_formateur: '22222222222222',
                    lieu_de_formation: '75019',
                    certifinfos: [
                        '80735'
                    ],
                    formacodes: [
                        '22403'
                    ]
                },
                source: {
                    type: 'intercarif',
                    numero_session: 'SE_XXXXXX',
                    numero_formation: 'F_XX_XX',
                    numero_action: 'AC_XX_XXXXXX',
                }
            }
        });
    });

    it('should reconcile comment without commentaire (null)', async () => {

        let db = await getTestDatabase();
        await Promise.all([
            importIntercarif(),
            insertIntoDatabase('comment', newComment({
                comment: null,
                training: {
                    formacode: '22403',
                    certifInfo: {
                        id: '80735',
                    },
                    organisation: {
                        siret: '22222222222222',
                    },
                    place: {
                        postalCode: '75019',
                    },
                },
            })),
        ]);

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.equal(session.avis.length, 1);
        assert.equal(session.avis[0].comment, undefined);
    });

    it('should reconcile comment without commentaire (undefined)', async () => {

        let db = await getTestDatabase();
        let comment = newComment({
            comment: undefined,
            training: {
                formacode: '22403',
                certifInfo: {
                    id: '80735',
                },
                organisation: {
                    siret: '22222222222222',
                },
                place: {
                    postalCode: '75019',
                },
            },
        });
        delete comment.comment;

        await Promise.all([
            importIntercarif(),
            insertIntoDatabase('comment', comment),
        ]);

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.equal(session.avis.length, 1);
        assert.equal(session.avis[0].comment, undefined);
    });

    it('should ignore not yet published comment', async () => {

        let db = await getTestDatabase();
        await Promise.all([
            importIntercarif(),
            insertIntoDatabase('comment', newComment({
                published: false,
                training: {
                    formacode: '22403',
                    certifInfo: {
                        id: '80735',
                    },
                    organisation: {
                        siret: '22222222222222',
                    },
                    place: {
                        postalCode: '75019',
                    },
                },
            })),
        ]);

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.equal(session.avis, undefined);
    });

    it('should reconcile rejected comment', async () => {

        let db = await getTestDatabase();
        await Promise.all([
            importIntercarif(),
            insertIntoDatabase('comment', newComment({
                published: false,
                rejected: true,
                comment: {
                    title: 'WTF',
                    text: 'WTF',
                },
                training: {
                    formacode: '22403',
                    certifInfo: {
                        id: '80735',
                    },
                    organisation: {
                        siret: '22222222222222',
                    },
                    place: {
                        postalCode: '75019',
                    },
                },
            })),
        ]);

        await generateSessions(db);

        let session = await db.collection('sessionsReconciliees').findOne();
        assert.equal(session.avis.length, 1);
        assert.equal(session.avis[0].rejected, true);
    });

    it('should create indexes', async () => {

        let db = await getTestDatabase();
        await Promise.all([
            importIntercarif(),
        ]);

        await generateSessions(db);

        let indexes = await db.collection('sessionsReconciliees').indexInformation();
        assert.deepEqual(indexes, {
            '_id_': [['_id', 1]],
            'numero_1': [['numero', 1]],
            'region_1': [['region', 1]],
            'score.nb_avis_1': [['score.nb_avis', 1]],
        });
    });

}));
